package org.usfirst.frc.team1014.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	public static final int SWERVE_DRIVE_A = 26;
	public static final int SWERVE_PIVOT_A = 25;
	
	public static final int SWERVE_DRIVE_B = 24;
	public static final int SWERVE_PIVOT_B = 23;
	
	public static final int SWERVE_DRIVE_C = 21;
	public static final int SWERVE_PIVOT_C = 22;
	
	public static final int SWERVE_DRIVE_D = 28;
	public static final int SWERVE_PIVOT_D = 27;
}
